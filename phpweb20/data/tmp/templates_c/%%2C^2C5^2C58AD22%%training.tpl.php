<h3>Workshop Content</h3>

<p>The training workshop will address the online therapeutic relationship and focus on text based computer mediated communication. The course will introduce participants to the skills involved in developing and fostering the online relationship. They will have an opportunity to practice these skills and discuss any related issues. The course will also explore the range of ethical issues that need to be considered when carrying out online work including security, privacy duty of care.</p>

<p>The training uses an interactive approach which facilitates discussion and dialogue using case examples, role play and exercises.</p>

 

<p>The Workshop Aims :</p>
<ul>
   <li>1. Become familiar with the computer mediated communication</li>
   <li>2. Teach techniques in developing a therapeutic relationship online</li>
   <li>3. Address the challenges for practitioners in ethical online work</li>
   <li>4. Skills Practice</li>
   <li>5. Implementing online counselling</li>
</ul>
 

 

