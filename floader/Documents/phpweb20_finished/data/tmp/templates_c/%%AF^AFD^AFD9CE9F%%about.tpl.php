			<h3><span class="backgroundGrey">Online Counselling</span></h3>
			
		    <p>

Online counselling has much in common with face-to-face counselling, except that you can engage in counselling at your own pace, in your own time by working with your counsellor online through writing.  Usually, online counselling doesn't take place in 'real time' and you and your counsellor post replies to each other over a period of time.  E-mail is not secure for online counselling that is why we use Secure Space a fully 128 bit encrypted environment, that provides water tight secure for confidential communications.

Many people find that the extra degree of anonymity offered by online counselling is a real benefit. As a client, you will be listened to non-judgmentally. Your counsellor will make every effort to understand things from your point of view, will be honest with you, and if at any time you have a question about the process of counselling, you will be able to ask.

</p>
<h3>How does it work?</h3>

<p>
 Secure Counselling.ie uses a password protected secure system which limits access exclusively to the counsellor and YOU. Your correspondence with the counsellor is strictly private, stored on secure protected servers with connections encrypted to the most rigorous professional standards.
</p>

<p>There are only 4 steps to begin counselling:</p>

  <p><b> 1.Choose your counsellor</b></p>

<p>Choose the counsellor you want to work with by clicking on the "Apply for Counselling" button on his/her page.</p>

  <p><b> 2.Complete short intake form</b></p>

<p>
Once you choose the counsellor you can complete a short intake form This involves completing contact details and writing your submission to the counsellor. Your submission should contain a detailed description of what is happening for you, the issues and difficulties you are having and what you would like help with. We would like to stress again that this is a direct, encrypted contact made to the counsellor only and is not accessible by any other 3rd parties.
</p>

 <p><b>3. Purchase your sessions</b></p>

<p>
The counsellor assesses the client's request and if he or she agrees to work with the client, an e-mail is sent to the client linking to a payment page to purchase one or more sessions with the counsellor. A secure private account is created for the counselling, accessible only by the client and the counsellor.
</p>
   
<p><b>4. Begin counselling</b></p>
<p>
Once you complete this process of applying and purchasing counselling you will receive the reply from the counsellor usually within one working day (always within three working days.) Each online session comprises one hour of the counsellor's time, just as in a face-to-face session. Generally, the counsellors will take time out to read what you have written to them, reflect on the issues that you are raising, the thoughts and emotions that you present with and then write a considered reply to you.
</p>

<p>
 All correspondence between you and the counsellor are stored in our secure online counselling space, called Secure Space. At any time you will be able to log on and read both your own submissions and the counsellors replies to you at any time via a standard web browser. The system is encrypted and certified secure and cannot be accessed by third parties.
</p>
<h3>What is online counselling suitable for?</h3>

<p>
Not every issue is suitable for online work, however, many issues are ideally suited to the online environment, benefiting from the alternative process of formulating questions and answers using the written word. In addition to providing a non-judgmental space for expressing or exploring personal issues, other situations which can be successfully worked with are:
</p>

<ul>
<li>Mild to moderate depression</li>
<li>Stress and anxiety</li>
<li>Relationship issues</li>
<li>Difficult personal circumstances such as bereavement, marital breakup, redundancy, illness</li>
<li>Personal exploration and development</li>
</ul>

<p>
When you apply for counselling we will request you to provide your name and contact information, as well as a second emergency contact. This information is taken for the purposes of ethical and professional practice on the part of the counsellors and their responsibility to you, as in face-to-face counselling.  All information is held in strictest confidence and is accessible only to the counsellor you have applied to work with.
</p>

<h3>What is Online Counselling not suitable for?</h3>

<p>
Online counselling is not suitable for everyone and for all issues.   It is important that for emergencies or crisis situations where, for example, you may be at risk of suicide or violence that you make contact with services such as:

    *

      The Samaritans 1850 60 90 90 (from within Ireland)
    *

      Befrienders International (International support numbers listed)
    *

      Your Doctor
    *

      Accident and Emergency at any hospital
</p>
<p>
Online counselling is not suitable if you have been previously diagnosed with a mental health disorder such as schizophrenia, where we recommend that you seek face-to-face support from your GP or psychiatrist. Online counselling is not suitable if you have serious alcohol or drug related difficulties, where these issues have impacted negatively upon your functioning in work and also socially.  In this case we recommend that you seek face-to-face support from specialised services such as the Alcohol treatment Unit, Baggot St. Hospital, Tel: (01) 6607838 (from within Ireland.)
</p>
<p>
If you are not sure whether online counselling is suitable for you or not, you can contact your preferred counsellor by clicking the "Apply for Counselling" button on the counsellor's page. This enables you to privately describe the issue(s) you would like to work with and to raise any questions you might have with your counsellor before committing to a paid session.
</p>
 
